    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String perm = "63";
        if (!bd.versiesta("select * from role_usuario inner join role on role.id=role_usuario.roleid inner join usuario on usuario.cod_user=role_usuario.usuarioid inner join role_permiso on role_permiso.roleid=role.id inner join permisos on permisos.id=role_permiso.permisoid where permisos.id=" + perm + " and role_usuario.cia=" + ControlSoftware.cia + " and usuario.cod_user=" + ControlSoftware.us)) {
            JOptionPane.showMessageDialog(null, "Usted no tiene permiso de modificar marcas de productos.");
            return;
        }
        if (Productos.getSelectedRowCount() == 0) {

            JOptionPane.showMessageDialog(null, "Ninguna fila seleccionada!");
            return;
        }
        if (bd.versiesta("select idsubcategoria from producto where idsubcategoria=" + Productos.getValueAt(Productos.getSelectedRow(), 0) + " and idcategoria="+Productos.getValueAt(Productos.getSelectedRow(), 2)+" and cia=" + ControlSoftware.cia)) {
            JOptionPane.showMessageDialog(null, "Este grupo ha sido asignado a un producto, por ende no se puede eliminar.");
            return;
        }
        bd.EjecutarConsultas("DELETE FROM SUBCATEGORIA WHERE CODSUBCATEGORIA=" + Productos.getValueAt(Productos.getSelectedRow(), 0) + " and idcategoria="+Productos.getValueAt(Productos.getSelectedRow(), 2)+" and cia=" + ControlSoftware.cia);
        jButton1.doClick();
    }//GEN-LAST:event_jButton4ActionPerformed

