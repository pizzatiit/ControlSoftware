    private void codclienteFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_codclienteFocusLost
        // TODO add your handling code here:
        if (bd.valor("select nombre from cliente where idcliente=" + codcliente.getText() + " and cia=" + ControlSoftware.cia).equals("")) {
            JOptionPane.showMessageDialog(null, "No existe!");
            codcliente.setText("0");
            descliente.setText("seleccione");

        } else {
            descliente.setText("" + bd.valor("select nombre from cliente where idcliente=" + codcliente.getText() + " and cia=" + ControlSoftware.cia));
        }
    }//GEN-LAST:event_codclienteFocusLost

