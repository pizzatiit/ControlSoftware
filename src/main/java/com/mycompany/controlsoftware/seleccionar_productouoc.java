    private void ProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ProductosMouseClicked
        // TODO add your handling code here:

        int pick = Productos.getSelectedRow();
        selected = Integer.parseInt("" + Productos.getValueAt(Productos.getSelectedRow(), 0));
        selcod.setText("" + Productos.getSelectedRow());
        if (evt.getClickCount() == 2 && !evt.isConsumed()) {
            evt.consume();
            for (int i = 0; i < ModificarOC.detalles.getRowCount(); i++) {
                if (ModificarOC.detalles.getValueAt(i, 0).toString().equals(Productos.getValueAt(pick, 0).toString())) {
                    JOptionPane.showMessageDialog(null, "PRODUCTO YA AGREGADO, SELECCIONE OTRO");
                    return;
                }
            }
            ModificarOC.idprod.setText(Productos.getValueAt(pick, 0).toString());
            ModificarOC.codprod.setText(Productos.getValueAt(pick, 1).toString());
            ModificarOC.desprod.setText(Productos.getValueAt(pick, 2).toString());
            ModificarOC.costofinaldolar.setText(Productos.getValueAt(pick, 12).toString());
            ModificarOC.costofinallempiras.setText(Productos.getValueAt(pick, 8).toString());
            ModificarOC.u1.setText(Productos.getValueAt(pick, 13).toString());
            ModificarOC.p1.setText(Productos.getValueAt(pick, 14).toString());
            ModificarOC.u2.setText(Productos.getValueAt(pick, 15).toString());
            ModificarOC.p2.setText(Productos.getValueAt(pick, 16).toString());
            ModificarOC.u3.setText(Productos.getValueAt(pick, 17).toString());
            ModificarOC.p3.setText(Productos.getValueAt(pick, 18).toString());
            ModificarOC.u4.setText(Productos.getValueAt(pick, 19).toString());
            ModificarOC.p4.setText(Productos.getValueAt(pick, 20).toString());
            ModificarOC.u5.setText(Productos.getValueAt(pick, 21).toString());
            ModificarOC.p5.setText(Productos.getValueAt(pick, 22).toString());
            ModificarOC.agregar.setEnabled(true);
            String x = "" + Productos.getValueAt(pick, 23).toString();
            String[] costoenletras1 = x.split("", 0);
            String costoenletras = "";
            for (int i = 0; i < costoenletras1.length; i++) {
                if (costoenletras1[i].equals("F")) {
                    costoenletras += "1";
                }
                if (costoenletras1[i].equals("R")) {
                    costoenletras += "2";
                }
                if (costoenletras1[i].equals("A")) {
                    costoenletras += "3";
                }
                if (costoenletras1[i].equals("N")) {
                    costoenletras += "4";
                }
                if (costoenletras1[i].equals("K")) {
                    costoenletras += "5";
                }
                if (costoenletras1[i].equals("L")) {
                    costoenletras += "6";
                }
                if (costoenletras1[i].equals("I")) {
                    costoenletras += "7";
                }
                if (costoenletras1[i].equals("H")) {
                    costoenletras += "8";
                }
                if (costoenletras1[i].equals("E")) {
                    costoenletras += "9";
                }
                if (costoenletras1[i].equals("C")) {
                    costoenletras += "0";
                }
                if (costoenletras1[i].equals(".")) {
                    costoenletras += ".";
                }
            }
            ModificarOC.cl = Double.parseDouble(costoenletras);
            ModificarOC.qty = Double.parseDouble(Productos.getValueAt(pick, 5).toString());
            dispose();
        }
    }//GEN-LAST:event_ProductosMouseClicked

