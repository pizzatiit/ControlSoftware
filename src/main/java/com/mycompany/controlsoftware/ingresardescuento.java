    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        if (new BigDecimal(totalfinal.getText()).doubleValue() <= 0) {
            JOptionPane.showMessageDialog(null, "No se puede aplicar un 100% de descuento, en dado caso mejor aplicar anulación.");
            return;
        }
        if (idfact.getText().isEmpty() || Double.parseDouble(idfact.getText()) <= 0) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar factura");
            return;
        }
        bd.EjecutarConsultas("update factura set totalfinal='" + totalfinal.getText() + "', descuento=" + descuento.getText() + " where idfactura=" + idfact.getText());
        jButton5.doClick();
        JOptionPane.showMessageDialog(null, "Datos actualizados correctamente.");
    }//GEN-LAST:event_jButton6ActionPerformed

