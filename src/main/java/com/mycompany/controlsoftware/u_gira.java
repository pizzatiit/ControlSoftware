    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        if (nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar nombre");
            return;
        }
        if (comodin.getText().isEmpty()) {
            comodin.setText("no especificado");
        }
        Item ven = (Item) vendedor.getSelectedItem();
        bd.EjecutarConsultas("update gira set gira='" + nombre.getText() + "', comodin='" + comodin.getText() + "', id_vendedor='" + ven.getId() + "' where id_gira=" + codigo + " and cia=" + ControlSoftware.cia);
        JOptionPane.showMessageDialog(null, "¬°Gira modificada con √©xito!");
        m_gira l = new m_gira();
        Dimension internalFrameSize = l.getSize();
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        l.setLocation((screenSize.width - internalFrameSize.width) / 2,
                (screenSize.height - internalFrameSize.height) / 2);
        DESKSOFTWARE.escritorio.add(l);
        l.show();
        dispose();
    }//GEN-LAST:event_jButton5ActionPerformed

