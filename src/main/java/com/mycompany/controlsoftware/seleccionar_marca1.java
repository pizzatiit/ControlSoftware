    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if(jTable1.getSelectedRowCount()==0){
            JOptionPane.showMessageDialog(null,"Debe seleccionar una fila previamente");
        }
        if (evt.getClickCount() == 2 && !evt.isConsumed()) {
                    evt.consume();
      modificarproducto.marca.setText(""+jTable1.getValueAt(jTable1.getSelectedRow(), 0));
       modificarproducto.descmarca.setText(""+jTable1.getValueAt(jTable1.getSelectedRow(), 1));
        dispose();
         }
    }//GEN-LAST:event_jTable1MouseClicked

