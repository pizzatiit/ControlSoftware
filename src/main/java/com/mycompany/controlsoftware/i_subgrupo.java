    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El grupo no puede quedar vacío.");
            return;
        }
        if (comodin.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El descuento no puede quedar vacío.");
            return;
        }
        if (comodin1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "La comision no puede quedar vacía.");
            return;
        }
        bd.EjecutarConsultas("INSERT INTO `subcategoria`(`subcategoria`, `idcategoria`, `cia`, `comision`, `descuento`, `estado`) VALUES ('" + nombre.getText() + "','" + codcat.getText() + "','" + ControlSoftware.cia + "','" + comodin1.getText() + "','" + comodin.getText() + "','1')");
        JOptionPane.showMessageDialog(null, "¡SubGrupo ingresado con éxito!");
        mantenimiento_subgrupos l = new mantenimiento_subgrupos();
        Dimension internalFrameSize = l.getSize();
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        l.setLocation((screenSize.width - internalFrameSize.width) / 2,
                (screenSize.height - internalFrameSize.height) / 2);
        DESKSOFTWARE.escritorio.add(l);
        l.show();
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void comodinKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_comodinKeyTyped
        // TODO add your handling code here:
        int key = evt.getKeyChar();
        if (!(key == '0' || key == '1' || key == '2' || key == '3' || key == '4' || key == '5' || key == '6' || key == '7' || key == '8' || key == '9' || key == '.') || (key == '.' && comodin.getText().contains("."))) {
            evt.consume();
        }
        if (comodin.getText().length() > 9) {
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
        }
    }//GEN-LAST:event_comodinKeyTyped

