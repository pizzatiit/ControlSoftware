    void autoajustar() {
        // Obtén el TableColumnModel de tu JTable
        TableColumnModel columnModel = Productos.getColumnModel();

// Itera sobre las columnas y ajusta su tamaño al contenido
        for (int columnIndex = 0; columnIndex < columnModel.getColumnCount(); columnIndex++) {
            TableColumn column = columnModel.getColumn(columnIndex);
            int width = 0;

            // Ajusta el ancho de la columna según el contenido de las celdas
            TableCellRenderer renderer = column.getHeaderRenderer();
            if (renderer == null) {
                renderer = Productos.getTableHeader().getDefaultRenderer();
            }
            Component headerComponent = renderer.getTableCellRendererComponent(Productos, column.getHeaderValue(), false, false, 0, columnIndex);
            width = headerComponent.getPreferredSize().width;

            for (int row = 0; row < Productos.getRowCount(); row++) {
                renderer = Productos.getCellRenderer(row, columnIndex);
                Component component = renderer.getTableCellRendererComponent(Productos, Productos.getValueAt(row, columnIndex), false, false, row, columnIndex);
                width = Math.max(width, component.getPreferredSize().width);
            }

            // Establece el ancho ajustado para la columna
            column.setPreferredWidth(width);
        }

// Ajusta el tamaño de las columnas al contenido
        Productos.getTableHeader().resizeAndRepaint();
    }

