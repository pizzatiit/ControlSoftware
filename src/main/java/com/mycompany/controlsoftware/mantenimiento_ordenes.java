    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (Productos.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Ninguna fila seleccionada!");
            return;
        }
        if (Productos.getValueAt(Productos.getSelectedRow(), 9).equals("FACTURADA")) {
            JOptionPane.showMessageDialog(null, "El pedido no puede ser eliminado puesto que ya ha sido facturado.");
            return;
        }
        if (Productos.getValueAt(Productos.getSelectedRow(), 9).equals("INGRESADA")) {
            JOptionPane.showMessageDialog(null, "El pedido no puede ser eliminado puesto que no ha sido anulado.");
            return;
        }
        Ingreso_de_credenciales dialog = new Ingreso_de_credenciales("82");
        dialog.setModal(true);
        dialog.setVisible(true);  // AQUI DEBE ESPERAR A QUE INGRESE LOS DATOS PARA HACER LA SIGUIENTE CONDICION
        if (dialog.paso == false) {
            return;
        }
        try {
            String id_op = "" + Productos.getValueAt(Productos.getSelectedRow(), 0);
            String estado = bd.valor("select estado from orden_de_pedido where idorden=" + Productos.getValueAt(Productos.getSelectedRow(), 0));
            if (!estado.equals("0")) {
                JOptionPane.showMessageDialog(null, "No se puede eliminar esta órden de pedido sin previa anulación.");
            } else {
                int opc = JOptionPane.showConfirmDialog(null, "Estas seguro que deseas eliminar esta orden de pedido?");
                if (opc == 0) {
                    bd.EjecutarConsultas("delete from detalle_orden_de_pedido where id_orden_de_pedido=" + id_op + " and cia=" + ControlSoftware.cia);
                    bd.EjecutarConsultas("delete from orden_de_pedido where idorden=" + id_op + " and cia=" + ControlSoftware.cia);
                    JOptionPane.showMessageDialog(null, "Este registro ha sido eliminado.");
                    jButton1.doClick();
                }
            }

        } catch (Exception e) {

        }
    }//GEN-LAST:event_jButton4ActionPerformed

