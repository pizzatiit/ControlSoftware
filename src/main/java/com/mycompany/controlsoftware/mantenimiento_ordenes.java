    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        if (Productos.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Ninguna fila seleccionada!");
            return;
        }
        if (Productos.getValueAt(Productos.getSelectedRow(), 9).equals("FACTURADA")) {
            JOptionPane.showMessageDialog(null, "El pedido no puede ser anulado puesto que ya ha sido facturado.");
            return;
        }
        if (Productos.getValueAt(Productos.getSelectedRow(), 9).equals("ANULADA")) {
            JOptionPane.showMessageDialog(null, "El pedido no puede ser anulado puesto que ya ha sido anulado.");
            return;
        }

        Ingreso_de_credenciales dialog = new Ingreso_de_credenciales("81");
        dialog.setModal(true);
        dialog.setVisible(true);  // AQUI DEBE ESPERAR A QUE INGRESE LOS DATOS PARA HACER LA SIGUIENTE CONDICION
        if (dialog.paso == false) {
            return;
        }

        try {
            String id_op = "" + Productos.getValueAt(Productos.getSelectedRow(), 0);
            String estado = bd.valor("select estado from orden_de_pedido where idorden=" + id_op + " and cia=" + ControlSoftware.cia);
            if (estado.equals("2")) {
                JOptionPane.showMessageDialog(null, "Esta orden de pedido no puede ser anulada puesto que ya ha sido procesada.");
                return;
            }
            if (estado.equals("0")) {
                JOptionPane.showMessageDialog(null, "Esta orden de pedido ya ha sido anulada.");
                return;
            }
            if (estado.equals("1")) {
                String motivo_anulacion = JOptionPane.showInputDialog(null, "Ingrese el motivo de anulación");
                String user = bd.valor("select idusuario from orden_de_pedido where idorden=" + id_op + " and cia=" + ControlSoftware.cia);

                motivo_anulacion += ", anulado, usuario que realizó la orden de pedido: " + bd.valor("select user from usuario where cod_user=" + user);
                String producto_a_rebajar[] = bd.Llenar("select idproducto from detalle_orden_de_pedido where id_orden_de_pedido=" + id_op + " and cia=" + ControlSoftware.cia, "idproducto");

                System.out.println("hola");
                if (!bd.valor("select cotid from orden_de_pedido where idorden=" + id_op + " and cia=" + ControlSoftware.cia).equals("0")) {
                    String cotid = bd.valor("select cotid from orden_de_pedido where idorden=" + id_op + " and cia=" + ControlSoftware.cia);
                    bd.EjecutarConsultas("update cotizaciones set estado=1 where id=" + cotid + " and cia=" + ControlSoftware.cia);
                }

                for (int i = 0; i < producto_a_rebajar.length; i++) {
                    Double qty = Double.parseDouble(bd.valor("select cantidad from detalle_orden_de_pedido where id_orden_de_pedido=" + id_op + " and idproducto=" + producto_a_rebajar[i] + " and cia=" + ControlSoftware.cia));
                    bd.EjecutarConsultas("update producto set reserva=reserva-" + qty + " where idproducto=" + producto_a_rebajar[i] + " and cia=" + ControlSoftware.cia);
                }
                bd.EjecutarConsultas("update orden_de_pedido set subtotal=0.0, isv=0.0, total=0.0, estado=0, idusuario='" + ControlSoftware.us + "', observacion='" + motivo_anulacion + "' where idorden=" + id_op + " and cia=" + ControlSoftware.cia);
                bd.EjecutarConsultas("update detalle_orden_de_pedido set preciounitario=0.0, preciovendido=0.0, costo=0.0, total=0.0, estado=0 where id_orden_de_pedido=" + id_op + " and cia=" + ControlSoftware.cia);
                jButton1.doClick();

                try {
                    BaseDeDatos con = new BaseDeDatos();
                    Connection conn = con.getConexion();
                    String path = "/comprobanteanulacion_op.jasper";
                    InputStream inputStream = getClass().getResourceAsStream(path);
                    JasperReport jasperReport = (JasperReport) JRLoader.loadObject(inputStream);

                    Map<String, Object> parametros = new HashMap<>();
                    parametros.put("factura", id_op);
                    parametros.put("user", ControlSoftware.cia);
                    JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parametros, conn);

                    // Crear el objeto JasperViewer y mostrar el reporte en pantalla
                    JasperViewer viewer = new JasperViewer(jasperPrint, false);
                    viewer.setVisible(true);

                } catch (JRException ex) {
                    System.out.println("Error al generar el reporte: " + ex.getMessage());
                }

            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton7ActionPerformed

