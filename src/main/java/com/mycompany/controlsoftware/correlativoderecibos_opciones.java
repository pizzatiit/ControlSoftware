    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int opc = 0;
        String dfecha = "";
        String hfecha = "";
        String dnumero = "";
        String hnumero = "";
        String pf = "0";
        String pn = "0";
        SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-MM-dd");
        Date date1;
        date1 = desdefecha.getDate();
        Date date2;
        date2 = hastafecha.getDate();
        if (sf.isSelected()) {
            dfecha = bd.valor("select min(fecha) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
            hfecha = bd.valor("select max(fecha) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
            dnumero = bd.valor("select CAST(MIN(CAST(numero_recibo AS UNSIGNED)) AS UNSIGNED) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
            hnumero = bd.valor("select CAST(MAX(CAST(numero_recibo AS UNSIGNED)) AS UNSIGNED) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
            pf = "0";
            pn = "0";
        }

        if (porgira.isSelected()) {
            if (!(date1 == null) && !(date2 == null)) {
                if (date2.compareTo(date1) == -1) {
                    JOptionPane.showMessageDialog(null, "La fecha final no puede ser mayor a la inicial.");
                    return;
                } else {
                    dfecha = "" + Date_Format.format(desdefecha.getDate());
                    hfecha = "" + Date_Format.format(hastafecha.getDate());
                    dnumero = bd.valor("select CAST(MIN(CAST(numero_recibo AS UNSIGNED)) AS UNSIGNED) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
                    hnumero = bd.valor("select CAST(MAX(CAST(numero_recibo AS UNSIGNED)) AS UNSIGNED) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
                    pf = "1";
                    pn = "0";
                }
            }else{
                JOptionPane.showMessageDialog(null,"Las fechas son inválidas");
                return;
            }

        }
        if (porvendedor.isSelected()) {
            if(desdenumero.getText().isEmpty()||hastanumero.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Los numeros son inválidos");
                return;
            }
            if(Integer.parseInt(desdenumero.getText())>Integer.parseInt(hastanumero.getText())){
                JOptionPane.showMessageDialog(null,"El numero final no puede ser menor");
                return;
            }
            dfecha = bd.valor("select min(fecha) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
            hfecha = bd.valor("select max(fecha) from recibopago where tipo=0 and cia=" + ControlSoftware.cia);
            dnumero = desdenumero.getText();
            hnumero = hastanumero.getText();
            pf = "0";
            pn = "1";
        }
            System.out.println(dfecha);
            System.out.println(hfecha);

        try {
            BaseDeDatos con = new BaseDeDatos();
            Connection conn = con.getConexion();
            // Cargar el reporte desde el archivo .jasper
            String path = "/correlativorecibos.jasper";
            InputStream inputStream = getClass().getResourceAsStream(path);
            JasperReport jasperReport = (JasperReport) JRLoader.loadObject(inputStream);

            Map<String, Object> parametros = new HashMap<>();
            parametros.put("dfecha", dfecha);
            parametros.put("hfecha", hfecha);
            parametros.put("dnumero", dnumero);
            parametros.put("hnumero", hnumero);
            parametros.put("pf", pf);
            parametros.put("pn", pn);
            parametros.put("cia", ControlSoftware.cia);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport, parametros, conn);

            JasperViewer viewer = new JasperViewer(jasperPrint, false);
            viewer.setVisible(true);

        } catch (JRException ex) {
            System.out.println("Error al generar el reporte: " + ex.getMessage());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

