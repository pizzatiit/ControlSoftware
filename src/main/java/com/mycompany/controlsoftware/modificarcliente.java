    private void codcatFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_codcatFocusLost
        // TODO add your handling code here:
        if (bd.valor("select departamento from departamento where id_departamento=" + codcat.getText()).equals("")) {
            JOptionPane.showMessageDialog(null, "No existe!");
            codcat.setText("0");
            descat.setText("seleccione");

        } else {
            descat.setText("" + bd.valor("select departamento from departamento where id_departamento=" + codcat.getText()));
        }
    }//GEN-LAST:event_codcatFocusLost

