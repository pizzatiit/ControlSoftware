    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try {
            if (identidad.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Identidad no puede estar vacío");
                return;
            }
            if (rtn.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "RTN no puede estar vacío");
                return;
            }
            if (nombre.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nombre no puede estar vacío");
                return;
            }
            if (telefono.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Telefono no puede estar vacío");
                return;
            }
            if (propietario.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Propietario no puede estar vacío");
                return;
            }
            if (direccion.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Dirección no puede estar vacía");
                return;
            } else {
                Item gi = (Item) gira.getSelectedItem();
                Item des = (Item) descuento.getSelectedItem();
                Item extr = (Item) extra.getSelectedItem();
                String state = "0";
                if (activo.isSelected()) {
                    state = "1";
                }
                LocalDate date = LocalDate.now();
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                bd.EjecutarConsultas("UPDATE `clientes` SET `nombre`='" + nombre.getText() + "',`rtn`='" + rtn.getText() + "',`identidad`='" + identidad.getText() + "',`propietario`='" + propietario.getText() + "',`encargado`='" + encargado.getText() + "',`direccion`='" + direccion.getText() + "',`departamento`='" + codcat.getText() + "',`ciudad`='" + codcat1.getText() + "',`numerotelefono`='" + telefono.getText() + "',`observacion`='" + observacion.getText() + "',`gira`='" + gi.getId() + "',`limite`='" + limite.getText() + "',`diaslimite`='" + dias.getText() + "',`descuento`='" + des.getId() + "',`extra`='" + extr.getId() + "',`usuarioingreso`='" + ControlSoftware.us + "',`estado`='" + state + "' WHERE idcliente=" + id + " and cia=" + ControlSoftware.cia);
                JOptionPane.showMessageDialog(null, "Cliente modificado con éxito");
                Clientes l = new Clientes();
                Dimension internalFrameSize = l.getSize();
                Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
                l.setLocation((screenSize.width - internalFrameSize.width) / 2,
                        (screenSize.height - internalFrameSize.height) / 2);
                DESKSOFTWARE.escritorio.add(l);
                l.show();
                dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error: " + e);
        }


    }//GEN-LAST:event_jButton6ActionPerformed

